const htmlLanguageCodes = {
  en: "en",
  zh: "zh-CN",
  es: "es"
};

function setLanguage(language) {
  const dictionary = translations[language] || translations.en;
  document.documentElement.lang = htmlLanguageCodes[language] || "en";
  document.title = dictionary.documentTitle;

  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.setAttribute("content", dictionary.metaDescription);
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const key = element.getAttribute("data-i18n-html");
    if (dictionary[key]) {
      element.innerHTML = dictionary[key];
    }
  });

  document.querySelectorAll("[data-lang-button]").forEach((button) => {
    const isActive = button.getAttribute("data-lang-button") === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("preferred-language", language);
}

document.querySelectorAll("[data-lang-button]").forEach((button) => {
  button.addEventListener("click", () => {
    const language = button.getAttribute("data-lang-button");
    setLanguage(language);
  });
});

const savedLanguage = localStorage.getItem("preferred-language");
const browserLanguage = navigator.language?.startsWith("zh")
  ? "zh"
  : navigator.language?.startsWith("es")
    ? "es"
    : "en";

setLanguage(savedLanguage && translations[savedLanguage] ? savedLanguage : browserLanguage);
